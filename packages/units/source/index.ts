/**
 * Binary Prefixes (IEC, 1024-based)
 *
 * @see {@link https://en.wikipedia.org/wiki/Binary_prefix}
 */
export enum BinaryPrefixes {
	/**
	 * Kibi
	 * 1024
	 */
	'Ki' = Math.pow(1024, 1),

	/**
	 * Mibi
	 * 1048576
	 */
	'Mi' = Math.pow(1024, 2),

	/**
	 * Gibi
	 * 1073741824
	 */
	'Gi' = Math.pow(1024, 3),

	/**
	 * Tebi
	 * 1099511627776
	 */
	'Ti' = Math.pow(1024, 4),

	/**
	 * Pebi
	 * 1125899906842624
	 */
	'Pi' = Math.pow(1024, 5),

	/**
	 * Exbi
	 * 1152921504606846976
	 */
	'Ei' = Math.pow(1024, 6),

	/**
	 * Zebi
	 * 1180591620717411303424
	 */
	'Zi' = Math.pow(1024, 7),

	/**
	 * Yobi
	 * 1208925819614629174706176
	 */
	'Yi' = Math.pow(1024, 8),
}

/**
 * Time conversion factors
 */
export enum TimeConversion {
	DAYS_PER_WEEK = 7,
	HOURS_PER_DAY = 24,
	MINUTES_PER_HOUR = 60,
	SECONDS_PER_MINUTE = 60,
	MILLISECONDS_PER_SECOND = 1000,
	MICROSECONDS_PER_MILLISECOND = 1000,
	NANOSECONDS_PER_MICROSECOND = 1000,

	HOURS_PER_WEEK = HOURS_PER_DAY * DAYS_PER_WEEK,

	MINUTES_PER_WEEK = MINUTES_PER_HOUR * HOURS_PER_WEEK,
	MINUTES_PER_DAY = MINUTES_PER_HOUR * HOURS_PER_DAY,

	SECONDS_PER_WEEK = SECONDS_PER_MINUTE * MINUTES_PER_WEEK,
	SECONDS_PER_DAY = SECONDS_PER_MINUTE * MINUTES_PER_DAY,
	SECONDS_PER_HOUR = SECONDS_PER_MINUTE * MINUTES_PER_HOUR,

	MILLISECONDS_PER_WEEK = MILLISECONDS_PER_SECOND * SECONDS_PER_WEEK,
	MILLISECONDS_PER_DAY = MILLISECONDS_PER_SECOND * SECONDS_PER_DAY,
	MILLISECONDS_PER_HOUR = MILLISECONDS_PER_SECOND * SECONDS_PER_HOUR,
	MILLISECONDS_PER_MINUTE = MILLISECONDS_PER_SECOND * SECONDS_PER_MINUTE,

	MICROSECONDS_PER_WEEK = MICROSECONDS_PER_MILLISECOND * MILLISECONDS_PER_WEEK,
	MICROSECONDS_PER_DAY = MICROSECONDS_PER_MILLISECOND * MILLISECONDS_PER_DAY,
	MICROSECONDS_PER_HOUR = MICROSECONDS_PER_MILLISECOND * MILLISECONDS_PER_HOUR,
	MICROSECONDS_PER_MINUTE = MICROSECONDS_PER_MILLISECOND *
		MILLISECONDS_PER_MINUTE,
	MICROSECONDS_PER_SECOND = MICROSECONDS_PER_MILLISECOND *
		MILLISECONDS_PER_SECOND,

	NANOSECONDS_PER_WEEK = NANOSECONDS_PER_MICROSECOND * MICROSECONDS_PER_WEEK,
	NANOSECONDS_PER_DAY = NANOSECONDS_PER_MICROSECOND * MICROSECONDS_PER_DAY,
	NANOSECONDS_PER_HOUR = NANOSECONDS_PER_MICROSECOND * MICROSECONDS_PER_HOUR,
	NANOSECONDS_PER_MINUTE = NANOSECONDS_PER_MICROSECOND *
		MICROSECONDS_PER_MINUTE,
	NANOSECONDS_PER_SECOND = NANOSECONDS_PER_MICROSECOND *
		MICROSECONDS_PER_SECOND,
	NANOSECONDS_PER_MILLISECOND = NANOSECONDS_PER_MICROSECOND *
		MICROSECONDS_PER_MILLISECOND,
}
